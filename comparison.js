// Create a variable named firstFavNumb and store your favorite number
let firstFavNumb = 7;

// Create a variable named secondFavNumb and store your second favorite number
let secondFavNumb = 10;

// Perform the following checks:

// Greater than
console.log("Greater than:", firstFavNumb > secondFavNumb); // false

// Less than
console.log("Less than:", firstFavNumb < secondFavNumb); // true

// Greater than or equal to
console.log("Greater than or equal to:", firstFavNumb >= secondFavNumb); // false

// Less than or equal to
console.log("Less than or equal to:", firstFavNumb <= secondFavNumb); // true

// Strict equality (===)
console.log("Strict equality (===):", firstFavNumb === secondFavNumb); // false

// Loose equality (==)
console.log("Loose equality (==):", firstFavNumb == secondFavNumb); // false

// Strict inequality (!==)
console.log("Strict inequality (!==):", firstFavNumb !== secondFavNumb); // true

// Loose inequality (!=)
console.log("Loose inequality (!=):", firstFavNumb != secondFavNumb); // true

// AND
console.log("AND:", firstFavNumb > 5 && secondFavNumb > 5); // true

// OR
console.log("OR:", firstFavNumb > 5 || secondFavNumb > 15); // true

// NOT
console.log("NOT:", !(firstFavNumb > 5)); // false
